'''
Знайти кількість парних елементів одновимірного масиву.
Постолович А.С. 122-В
'''
import numpy as np
import random

A = np.zeros(20) #створюємо пустий масив
k = 0 #вводимо лічильник парних чисел
for i in range(20):
    A[i] = random.randint(0, 40) #заповнюємо масив
    if A[i]%2 == 0:
        k += 1 # перераховуємо парні числа
print(f'Кількість парних елементів масиву -  {k}') #виводимо дані