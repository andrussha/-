import numpy as np
import random

A = np.zeros(20) #створюємо пустий масив
for i in range(20):
    A[i] = random.randint(0,100) #заповнюємо масив
for i in range(20):
    if A[i] > A[i+1]: #перевіряємо чи є він упорядкованим
        print('Масив упорядкований по спаданню')
        break
    else:
        print('Масив неупорядкований по спаданню ')
        break