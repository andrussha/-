'''
Створіть масив А [1..12] за допомогою генератора випадкових чисел з
елементами від -20 до 10 і виведіть його на екран. Замініть всі від’ємні елементи
масиву числом 0.
Постолович А. С. 122-В
'''
import numpy as np
import random

A = np.zeros(12) #створюємо пустий масив
for i in range(12):
    A[i] = random.randint(-20,10) #заповнюємо його числами
for j in range(12):
    if A[j] < 0:
        A[j] = 0 #замінюємо всі від'ємні числа на 0
    print(A[j], end=', ')